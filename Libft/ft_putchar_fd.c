#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

/*Envía el carácter ’c’ al file descriptor
especificado.
file descriptor es un número entero que identifica
un archivo abierto por un proceso.*/