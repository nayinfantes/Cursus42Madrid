#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}

/*Envía la string ’s’ al file descriptor dado,
seguido de un salto de línea.
file descriptor es un número entero que
identifica un archivo abierto por un proceso.*/