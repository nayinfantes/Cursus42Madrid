#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
		return ;
	write(fd, s, ft_strlen(s));
}

/*Envía la string ’s’ al file descriptor
especificado.
file descriptor es un número entero que
identifica un archivo abierto por un proceso.*/